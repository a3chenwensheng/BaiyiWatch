package com.baiyi.watch.model;

public class FAQ {
	private String f;
	private String q;

	public FAQ() {
	}

	public FAQ(String f, String q) {
		this.f = f;
		this.q = q;
	}

	public String getF() {
		return f;
	}

	public void setF(String f) {
		this.f = f;
	}

	public String getQ() {
		return q;
	}

	public void setQ(String q) {
		this.q = q;
	}

}